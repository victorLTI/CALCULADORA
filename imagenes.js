function imprimir(){
    let op = parseInt(document.getElementById("num").value);
    let img = document.getElementById("imagen");
if(op==1){
    imprimir.value=img.src="recursos/tilin.jpg";}
else if(op==2){
    imprimir.value=img.src="recursos/tilin2.jpg";}
else if(op==3){
    imprimir.value=img.src="recursos/tilin3.jpeg";}
}
